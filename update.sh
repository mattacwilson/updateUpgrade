#Quick script to update and upgrade Linux
#Created by Matthew Wilson

#!/bin/bash

echo "+++++++++++++++++++++++++++++++++++++++++++++++++++++++"
echo "======================================================="
echo   "\n$(date "+%d-%m-%Y --- %T") --- Starting work\n"  
echo "=======================================================" 
echo "+++++++++++++++++++++++++++++++++++++++++++++++++++++++"

apt-get update

echo "+++++++++++++++++++++++++++++++++++++++++++++++++++++++"
echo "=======================================================" 
echo  "\n$(date "+%T") \t apt-get update complete"
echo "=======================================================" 
echo "+++++++++++++++++++++++++++++++++++++++++++++++++++++++"

sleep 1

 
apt-get -y upgrade

echo "+++++++++++++++++++++++++++++++++++++++++++++++++++++++"
echo "=======================================================" 
echo  "\n$(date "+%T") \t apt-get upgrade complete"
echo "=======================================================" 
echo "+++++++++++++++++++++++++++++++++++++++++++++++++++++++"

sleep 1

apt-get -y autoremove
apt-get autoclean

echo "+++++++++++++++++++++++++++++++++++++++++++++++++++++++"
echo "======================================================="
echo "Script terminated at: \t $(date "+%T")"
echo "======================================================="
echo "+++++++++++++++++++++++++++++++++++++++++++++++++++++++"
